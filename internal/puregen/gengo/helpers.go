// returns unsafe string, so do not store it anywhere
func Json2ReadUnion(typeName string, in *jlexer.Lexer) (string, []byte, error) {
	if in == nil {
		return "", nil, ErrorInvalidJSON(typeName, "expected json object or string")
	}
	if in.CurrentToken() == jlexer.TokenString {
		return in.UnsafeString(), nil, nil
	}

	var valueFound bool
	var valueSlice []byte

	var typeFound bool
	var typeValue string

	in.Delim('{')
	if !in.Ok() {
		return "", nil, in.Error()
	}
	for !in.IsDelim('}') {
		key := in.UnsafeFieldName(true)
		in.WantColon()
		switch key {
		case "value":
			if valueFound {
				return "", nil, ErrorInvalidJSONWithDuplicatingKeys(typeName, "value")
			}
			valueSlice = in.Raw()
			valueFound = true
		case "type":
			if typeFound {
				return "", nil, ErrorInvalidJSONWithDuplicatingKeys(typeName, "type")
			}
			typeValue = in.UnsafeString()
			typeFound = true
		default:
			return "", nil, ErrorInvalidJSON(typeName, "unexpected field '" + key + "' in union")
		}

		in.WantComma()
	}
	in.Delim('}')
	if !in.Ok() {
		return "", nil, in.Error()
	}
	
	if !typeFound {
		return "", nil, ErrorInvalidJSON(typeName, "field 'type' is absent")
	}

	return typeValue, valueSlice, nil
}

func Json2ReadMaybe(typeName string, in *jlexer.Lexer) (bool, []byte, error) {
	if in == nil {
		return false, nil, nil
	}
	var valueFound bool
	var valueSlice []byte

	var okFound bool
	var okValue bool

	in.Delim('{')
	if !in.Ok() {
		return false, nil, ErrorInvalidJSON(typeName, "expected json object")
	}
	for !in.IsDelim('}') {
		key := in.UnsafeFieldName(true)
		in.WantColon()
		switch key {
		case "value":
			if valueFound {
				return false, nil, ErrorInvalidJSONWithDuplicatingKeys(typeName, "value")
			}
			valueSlice = in.Raw()
			valueFound = true
		case "ok":
			if okFound {
				return false, nil, ErrorInvalidJSONWithDuplicatingKeys(typeName, "ok")
			}
			okValue = in.Bool()
			okFound = true
		default:
			return false, nil, ErrorInvalidJSON(typeName, "unexpected field '" + key + "' in maybe")
		}

		in.WantComma()
	}
	in.Delim('}')
	if !in.Ok() {
		return false, nil, in.Error()
	}

	if okFound && !okValue && valueSlice != nil {
		return false, nil, ErrorInvalidJSON(typeName, "field 'ok' is false but field 'value' is presented in maybe")
	}
	if !okFound && valueSlice != nil {
		okValue = true
	}
	return okValue, valueSlice, nil
}

func Json2ReadBool(in *jlexer.Lexer, dst *bool) error {
	if in == nil {
		*dst = false
		return nil
	}
	*dst = in.Bool()
	if !in.Ok() {
		return in.Error()
	}
	return nil
}

func Json2ReadString(in *jlexer.Lexer, dst *string) error {
	if in == nil {
		*dst = ""
		return nil
	}

	switch in.CurrentToken() {
	case jlexer.TokenString:
		*dst = in.String()
	case jlexer.TokenDelim:
		var findValue = false

		in.Delim('{')
		if !in.Ok() {
			return in.Error()
		}
		for !in.IsDelim('}') {
			key := in.UnsafeFieldName(true)
			in.WantColon()
			switch key {
			case "` + binaryStringObjectKey + `":
				if findValue {
					return errors.New("` + binaryStringObjectKey + ` repeats several times") 
				}
				*dst = string(in.Bytes())
				findValue = true
			default:
				return errors.New("unexpected field '" + key + "'")
			}

			in.WantComma()
		}
		in.Delim('}')
		if !in.Ok() {
			return in.Error()
		}

		if !findValue {
			return errors.New("field ` + binaryStringObjectKey + ` is absent")
		}
	default:
		return errors.New("invalid json for string")
	}
	return nil
}

